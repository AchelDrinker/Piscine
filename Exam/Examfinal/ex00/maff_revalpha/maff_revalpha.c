#include <unistd.h>

void ft_putchar(char c)
{
	write(1, &c, 1);
}

int main()
{
	int i = 'z';

	while (i >= 'a')
	{
		ft_putchar(i);
		i--;
		ft_putchar(i - 32);
		i--;
	}
	write(1, "\n", 1);
	return 0;
}
