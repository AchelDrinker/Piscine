void ft_putchar(char c)
{
	write(1, &c, 1);
}

char *ft_rev_print(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	i = i - 1;
	while (i >= 0)
	{
		ft_putchar(str[i]);
		i--;
	}
	return(str);
}
