int	ft_sqrt(int nb)
{
	int	i;
	int	ii;

	if (nb < 0)
		return (0);
	i = 0;
	ii = 1;
	while (nb > 0)
	{
		nb -= ii;
		ii += 2;
		i++;
	}
	if (nb == 0)
		return (i);
	else
		return (0);
}
