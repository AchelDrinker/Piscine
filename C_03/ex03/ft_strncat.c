char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int	i;
	int	c;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	c = 0;
	while (src[c] != '\0' && nb > 0)
	{
		dest[i] = src[c];
		i++;
		c++;
		nb--;
	}
	dest[i] = '\0';
	return (dest);
}
